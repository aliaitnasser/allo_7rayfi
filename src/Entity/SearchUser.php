<?php

namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;

class SearchUser
{
    
    private $City;
    private $job;
    
    
    public function getCity(): ?string
    {
        return $this->City;
    }

    public function setCity(string $City): self
    {
        $this->City = $City;

        return $this;
    }

    public function getJob(): ?string
    {
        return $this->job;
    }

    public function setJob(string $job): self
    {
        $this->job = $job;

        return $this;
    }
}
